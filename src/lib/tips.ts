/**
 * ヒスイ拾いワンポイント。
 * 日付ベースで日替わり表示する。将来はNotionやCMS化しやすいよう配列で分離。
 */
export const HISUI_TIPS: readonly string[] = [
  "ヒスイは白っぽく、光に透かすと縁がうっすら緑や青に見えることがあります。濡らすと特徴が分かりやすくなります。",
  "波打ち際の小石が集まる筋（打ち上げライン）を重点的に探すと見つかりやすいです。",
  "ヒスイは比重が重く硬いのが特徴。似た石（キツネ石など）より手にずっしり重く感じます。",
  "荒天の翌日、波が落ち着いたタイミングは新しい石が打ち上がりやすい狙い目です。",
  "朝日を背にして歩くと、石の透明感や照りを見つけやすくなります。",
  "拾う量はほどほどに。自然環境と他の人のためにマナーを守って楽しみましょう。",
  "偏光サングラスがあると水面のギラつきが抑えられ、石が見やすくなります。",
  "濡れた軍手やピンセットがあると、冷たい季節でも快適に探せます。",
  "満潮・干潮の時間を事前に確認し、干潮前後の浜が広がる時間帯を狙いましょう。",
  "ヒスイは角が取れて丸みを帯びていることが多いです。角ばった石より要チェック。",
] as const;

/** 日付から決定的にワンポイントを選ぶ（同じ日は同じTIP）。 */
export function getTipOfDay(dateIso: string): string {
  const seed = dateIso
    .split("-")
    .reduce((acc, part) => acc + Number(part), 0);
  const index = seed % HISUI_TIPS.length;
  return HISUI_TIPS[index] ?? HISUI_TIPS[0]!;
}
